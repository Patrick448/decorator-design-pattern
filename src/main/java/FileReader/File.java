package FileReader;

public class File {
    private String contents;
    private String path;

    public String getContents() {
        return contents;
    }

    public String getPath() {
        return path;
    }

    public void setContents(String contents) {
        this.contents = contents;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
